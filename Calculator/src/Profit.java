public class Profit {
    private double unitProfit;
    private double grossProfit;
    private double nettProfit;
    private double profitMargin;

    public double getUnitProfit() {
        return unitProfit;
    }

    public void setUnitProfit(double unitProfit) {
        this.unitProfit = unitProfit;
    }

    public double getGrossProfit() {
        return grossProfit;
    }

    public void setGrossProfit(double grossProfit) {
        this.grossProfit = grossProfit;
    }

    public double getNettProfit() {
        return nettProfit;
    }

    public void setNettProfit(double nettProfit) {
        this.nettProfit = nettProfit;
    }

    public double getProfitMargin() {
        return profitMargin;
    }

    public void setProfitMargin(double profitMargin) {
        this.profitMargin = profitMargin;
    }
}
